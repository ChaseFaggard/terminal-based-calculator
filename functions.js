// Calculator functions

const functions = {
    add(a, b) { return a + b; },
    subtract(a, b) { return a - b; },
    multiply(a, b) { return a * b; },
    divide(a, b) { return a / b; },
    remainder(a, b) { return a % b; },
    exponent(a, b) { return a ** b; }
}

module.exports = {functions }; // Export module